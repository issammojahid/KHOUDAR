export function getRandomLetter(difficulty: "easy" | "normal" | "hard" = "normal"): string {
  const pool = difficulty === "easy" ? EASY_LETTERS : ARABIC_LETTERS;
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getAIAnswer(letter: string, category: string, difficulty: "easy" | "normal" | "hard"): string {
  const words = arabicWordsDB[letter]?.[category] || [];
  if (words.length === 0) return "";

  let chance: number;
  if (difficulty === "easy") chance = 0.35;
  else if (difficulty === "normal") chance = 0.65;
  else chance = 0.92;

  if (Math.random() > chance) return "";

  return words[Math.floor(Math.random() * words.length)];
}
